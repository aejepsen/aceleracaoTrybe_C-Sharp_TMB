public class Program
{
    public static void Main(string[] args)
    {
      // ++ incrementa +1
      // -- decrementa -1
      // += incrementa o valor da variável
      // -= decrementa o valor da variável
      // *= multiplica o valor da variável
      // /= divide o valor da variável
      // %= pega o resto da divisão do valor da variável
      int a = 10;
      a += 5;
      Console.WriteLine(a);
      // 15
      a = 10;
      a -= 5;
      Console.WriteLine(a);
      // 5
      a = 10;
      a *= 5;
      Console.WriteLine(a);
      // 50
      a = 10;
      a /= 5;
      Console.WriteLine(a);
      // 2
      a = 10;
      a %= 5;
      Console.WriteLine(a);
      // 0

    }
}
